    public void regresarCambio(){
        Integer i=500;
        Integer r=0;
        Integer a=credito-17;
        Integer cuenta=0;
        while(i>0&&a!=0){
            Integer b=a-i;
            if((b>=0)&&(evaluarexistencia(i))){
            r++;
            agregarcuenta(i);
            cuenta+=i;
            a=b;
            }else{
            i=evaluari(i);
            r=0;
            }
            System.out.println(a+"/"+b+"/"+i+"/"+cuenta+"/"+r);
        }
        imprimircambio();
        cc--;
        lblCcc.setText(cc+" Botellas de coca");
        credito=0;
        lblPnl.setText("<html>Bienvenido usuario <p>Porvafor elija su producto<p>");
        resetcambio();
        actualizarfondos();
    }
    
